import request from 'supertest';
import { beforeAll, describe, expect, it } from 'vitest';

import { app } from '@/app';
import { createAndAuthenticateUser } from '@/utils/tests/factories/core/create-and-authenticate-user.e2e';
import { createAndSetupTenant } from '@/utils/tests/factories/core/create-and-setup-tenant.e2e';
import { createTaskBoard } from '@/utils/tests/factories/tasks/create-task-board-test-data.e2e';
import { createTaskCard } from '@/utils/tests/factories/tasks/create-task-card-test-data.e2e';

describe('Create Comment (E2E)', () => {
  let tenantId: string;

  beforeAll(async () => {
    await app.ready();
    const { tenantId: tid } = await createAndSetupTenant();
    tenantId = tid;
  });

  it('should create a comment on a card', async () => {
    const { token, user } = await createAndAuthenticateUser(app, { tenantId });
    const userId = user.user.id;

    const { board, columns } = await createTaskBoard(tenantId, userId);
    const card = await createTaskCard(board.id, columns[0].id, userId);

    const response = await request(app.server)
      .post(`/v1/tasks/boards/${board.id}/cards/${card.id}/comments`)
      .set('Authorization', `Bearer ${token}`)
      .send({ content: 'Este é um comentário de teste.' });

    expect(response.status).toBe(201);
    expect(response.body).toHaveProperty('comment');
    expect(response.body.comment.content).toBe(
      'Este é um comentário de teste.',
    );
    expect(response.body.comment.cardId).toBe(card.id);
  });
});
